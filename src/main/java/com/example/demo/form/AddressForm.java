package com.example.demo.form;

public class AddressForm {

    private String city;
    private String street;

    public AddressForm() {
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }
}
